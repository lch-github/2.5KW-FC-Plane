void MCP41010_write_bite(void)
{
	mcp41010[0] = 17; //对电位器0写数据
	//0~255 ---> 10K~0K
	mcp41010[1] = 0; 
	
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_8,GPIO_PIN_RESET);
	
	HAL_SPI_Transmit(&hspi1,mcp41010,2,1000);
	
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_8,GPIO_PIN_SET);
	
}

//冒泡排序并计算出每秒升温速率
uint16_t bubble_sort(uint16_t a[],int n)//n为数组a的元素个数
{
    //一定进行N-1轮比较
    for(int i=0; i<n-1; i++)
    {
        //每一轮比较前n-1-i个，即已排序好的最后i个不用比较
        for(int j=0; j<n-1-i; j++)
        {
            if(a[j] > a[j+1])
            {
                int temp = a[j];
                a[j] = a[j+1];
                a[j+1]=temp;
            }
        }
    }
	
	max_temperature = a[n-1];
	min_temperature = a[0];
	
	return ((a[n-1] - a[0]) / n); //精度0.01℃
	
}

